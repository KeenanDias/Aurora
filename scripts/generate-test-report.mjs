import { execSync } from "child_process"
import { writeFileSync } from "fs"

const timestamp = new Date().toISOString()
const dateStr = new Date().toLocaleDateString("en-US", {
  year: "numeric",
  month: "long",
  day: "numeric",
  hour: "2-digit",
  minute: "2-digit",
})

let vitestOutput = ""
let vitestPassed = false

try {
  vitestOutput = execSync("npx vitest run --reporter=verbose 2>&1", {
    encoding: "utf-8",
    timeout: 60000,
  })
  vitestPassed = true
} catch (e) {
  vitestOutput = e.stdout || e.message
  vitestPassed = false
}

// Parse test results from verbose output — grab the "Tests" summary line, not "Test Files"
const testsLine = vitestOutput.match(/Tests\s+(\d+)\s*passed(?:.*?(\d+)\s*failed)?.*?\((\d+)\)/i)
const totalPassed = testsLine ? parseInt(testsLine[1]) : 0
const totalFailed = testsLine && testsLine[2] ? parseInt(testsLine[2]) : 0
const totalTests = totalPassed + totalFailed

// Extract test file results
const suiteResults = []
const suiteRegex = /[✓✗✓×√]?\s*(.*?\.test\.ts)\s/g
const fileLines = vitestOutput.split("\n")

// Parse individual test lines
const testLines = fileLines.filter(
  (l) => l.includes("✓") || l.includes("×") || l.includes("✗") || l.includes("√") || l.includes("PASS") || l.includes("FAIL")
)

// Categorize by security gate
const gates = {
  "Financial Accuracy": {
    tests: [],
    file: "safe-to-spend.test.ts",
    status: "UNKNOWN",
  },
  "Data Encryption": {
    tests: [],
    file: "encryption.test.ts",
    status: "UNKNOWN",
  },
  "Account Verification": {
    tests: [],
    file: "verification.test.ts",
    status: "UNKNOWN",
  },
  "Security Audit": {
    tests: [],
    file: "security.test.ts",
    status: "UNKNOWN",
  },
}

for (const [gateName, gate] of Object.entries(gates)) {
  if (vitestOutput.includes(gate.file)) {
    const hasFailInFile =
      vitestOutput.includes(`FAIL`) &&
      vitestOutput.split(gate.file).length > 1 &&
      vitestOutput.split(gate.file)[1]?.split("\n").slice(0, 5).some((l) => l.includes("×") || l.includes("✗") || l.includes("FAIL"))
    gate.status = hasFailInFile ? "FAIL" : "PASS"
  }
}

// Overall status
const overallStatus = vitestPassed ? "PASS" : "FAIL"
const statusBadge = overallStatus === "PASS" ? "ALL GATES PASSED" : "GATES FAILED — SEE BELOW"

const report = `# Aurora Test Report — Security & Accuracy Audit

**Generated:** ${dateStr}
**Status:** ${statusBadge}
**Tests:** ${totalPassed} passed, ${totalFailed} failed, ${totalTests} total

---

## Security Gates

| Gate | Status | Description |
|------|--------|-------------|
| Financial Accuracy | ${gates["Financial Accuracy"].status} | Safe-to-Spend formula correctness, edge cases, deficit carry-forward |
| Data Encryption | ${gates["Data Encryption"].status} | AES-256-GCM encrypt/decrypt integrity, IV randomness, tamper detection |
| Account Verification | ${gates["Account Verification"].status} | Canadian bank alias matching, CPA 005 codes, mask validation |
| Security Audit | ${gates["Security Audit"].status} | No secrets in client bundles, file upload validation, auth checks |

---

## Detailed Results

\`\`\`
${vitestOutput.slice(-3000)}
\`\`\`

---

## Audit Checklist

- [${overallStatus === "PASS" ? "x" : " "}] All financial calculations are mathematically accurate
- [${gates["Data Encryption"].status === "PASS" ? "x" : " "}] AES-256-GCM encryption produces identical output on decrypt
- [${gates["Data Encryption"].status === "PASS" ? "x" : " "}] Tampered ciphertext is rejected (GCM authentication)
- [${gates["Data Encryption"].status === "PASS" ? "x" : " "}] Random IVs used (no IV reuse)
- [${gates["Account Verification"].status === "PASS" ? "x" : " "}] Canadian bank verification matches institution aliases
- [${gates["Account Verification"].status === "PASS" ? "x" : " "}] Account mask mismatches are correctly flagged
- [${gates["Security Audit"].status === "PASS" ? "x" : " "}] No secrets exposed in client-side code
- [${gates["Security Audit"].status === "PASS" ? "x" : " "}] File upload validates PDF type and size
- [${gates["Security Audit"].status === "PASS" ? "x" : " "}] Cron endpoints require authorization

---

*Report generated by Aurora Test Suite v1.0*
*Timestamp: ${timestamp}*
`

writeFileSync("TEST_REPORT.md", report)
console.log(`\nTest report written to TEST_REPORT.md`)
console.log(`Status: ${statusBadge}`)
console.log(`Tests: ${totalPassed} passed, ${totalFailed} failed`)
